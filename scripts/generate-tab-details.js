function domainKey(menu) {
  const match = menu.code.match(/^ADM-([A-Z]+)-/);
  return match?.[1] && domainPolicies[match[1]] ? match[1] : "DEFAULT";
}

function hasAny(text, words) {
  return words.some((word) => text.includes(word));
}

function profileFor(tabName) {
  return tabTypeProfiles.find((profile) => profile.words.length && hasAny(tabName, profile.words)) || tabTypeProfiles.find((profile) => profile.id === "context");
}

function combine(base, extra) {
  return `${base}, ${extra}`;
}

